def _sha256(path: Path) -> str:
    return hashlib.sha256(path.read_bytes()).hexdigest()


def normalized_target_path(target_root: Path) -> str:
    """The exact target-path string the binding records and hashes."""
    return str(Path(target_root).expanduser().resolve())


def path_sha256(path_str: str) -> str:
    return hashlib.sha256(path_str.encode("utf-8")).hexdigest()


def build_binding_fields(
    target_root: Path,
    *,
    source_ref: str,
    source_sha256: str | None = None,
) -> dict:
    """Binding metadata for source_package.json.

    `target_path_sha256` hashes the recorded `target_path` string itself so a
    hand-edited or truncated binding is detectable as malformed, independent of
    whether the recorded path happens to resolve.
    """
    target_path = normalized_target_path(target_root)
    return {
        "created_by": BINDING_CREATED_BY,
        "created_at": utc_now(),
        "binding_schema_version": BINDING_SCHEMA_VERSION,
        "target_path": target_path,
        "target_path_sha256": path_sha256(target_path),
        "source_ref": source_ref,
        "source_sha256": source_sha256,
    }


def _source_file_sha256(ref: str) -> str | None:
    """Hash the source HLD file content when the ref is an existing file."""
    try:
        path = Path(ref).expanduser()
        if path.is_file():
            return _sha256(path)
    except OSError:
        return None
    return None


@dataclass(frozen=True)
class ManifestEntry:
    name: str
    filename: str
    present: bool
    sha256: str | None


@dataclass
class SourcePackageValidation:
    missing: list[str] = field(default_factory=list)
    hash_mismatches: list[str] = field(default_factory=list)
    semantic_errors: list[str] = field(default_factory=list)

    @property
    def ok(self) -> bool:
        return not self.missing and not self.hash_mismatches


def compute_manifest(source_dir: Path) -> dict[str, dict]:
    """Hash every authoritative content file. Index files are excluded so the
    manifest never tries to hash itself."""
    entries: dict[str, dict] = {}
    named = dict(AUTHORITATIVE_FILES)
    named["constitution_proposal"] = CONSTITUTION_PROPOSAL_FILE
    for name, filename in named.items():
        path = source_dir / filename
        present = path.is_file()
        entries[name] = {
            "filename": filename,
            "present": present,
            "sha256": _sha256(path) if present else None,
        }
    return entries


def write_source_manifest(source_dir: Path) -> dict[str, dict]:
    manifest = compute_manifest(source_dir)
    write_json_dict(source_dir / SOURCE_MANIFEST_FILE, {
        "schema_version": SCHEMA_VERSION,
        "files": manifest,
    })
    return manifest


def build_source_package_metadata(
    *,
    hld_source_ref: str,
    state: str,
    manifest: dict[str, dict],
    binding: dict | None = None,
) -> dict:
    metadata = {
        "schema_version": SCHEMA_VERSION,
        "hld_source_ref": hld_source_ref,
        "state": state,
        "manifest_file": SOURCE_MANIFEST_FILE,
        "mirror_files": list(MIRROR_FILES),
        "required_files": list(REQUIRED_FILES),
        "file_count": len(manifest),
    }
    if binding:
        metadata.update(binding)
    return metadata


def write_source_package(
    source_dir: Path,
    *,
    hld_source_ref: str,
    state: str,
    target_root: Path | None = None,
    source_sha256: str | None = None,
) -> dict:
    """Write source_manifest.json then source_package.json. Returns the metadata.

    When `target_root` is given the metadata carries target/source binding
    fields; discovery only trusts a bound package whose binding matches. A
    call without `target_root` writes an unbound (legacy-shaped) package that
    discovery reports as UNBOUND_LEGACY and never fully trusts.
    """
    manifest = write_source_manifest(source_dir)
    binding = (
        build_binding_fields(target_root, source_ref=hld_source_ref, source_sha256=source_sha256)
        if target_root is not None
        else None
    )
    metadata = build_source_package_metadata(
        hld_source_ref=hld_source_ref, state=state, manifest=manifest, binding=binding
    )
    write_json_dict(source_dir / SOURCE_PACKAGE_FILE, metadata)
    return metadata


def _validate_receipt_semantics(source_dir: Path) -> list[str]:
    """Validate active-spec receipt consistency with scope, files, and ledger.

    Returns a list of deterministic semantic error strings. Never raises.
    """
    import json

    errors: list[str] = []
    scope_path = source_dir / AUTHORITATIVE_FILES["hld_coverage_scope"]
    receipt_path = source_dir / AUTHORITATIVE_FILES["active_spec_materialization_receipt"]

    scope_present = scope_path.is_file()
    receipt_present = receipt_path.is_file()

    if not scope_present and not receipt_present:
        return errors

    if receipt_present and not scope_present:
        errors.append("active_spec_materialization_receipt present but hld_coverage_scope.json missing")
        return errors

    try:
        scope = json.loads(scope_path.read_text(encoding="utf-8"))
    except (json.JSONDecodeError, OSError):
        errors.append("hld_coverage_scope.json is malformed")
        return errors

    if not isinstance(scope, dict):
        errors.append("hld_coverage_scope.json is not a JSON object")
        return errors

    scope_mode = scope.get("coverage_scope")

    if scope_mode == "FULL_HLD":
        if receipt_present:
            errors.append("active_spec_materialization_receipt present in FULL_HLD mode")
        return errors

    if scope_mode not in {"FULL_HLD", "ACTIVE_SPEC"}:
        errors.append("hld_coverage_scope.json coverage_scope must be FULL_HLD or ACTIVE_SPEC")
        return errors

    if not receipt_present:
        errors.append("ACTIVE_SPEC scope requires active_spec_materialization_receipt.json")
        return errors

    try:
        receipt = json.loads(receipt_path.read_text(encoding="utf-8"))
    except (json.JSONDecodeError, OSError):
        errors.append("active_spec_materialization_receipt.json is malformed JSON")
        return errors

    if not isinstance(receipt, dict):
        errors.append("active_spec_materialization_receipt.json is not a JSON object")
        return errors

    if receipt.get("schema_version") != 1:
        errors.append("receipt schema_version must be 1")
    if receipt.get("receipt_type") != "ACTIVE_SPEC_SOURCE_PACKAGE_RENDER":
        errors.append("receipt receipt_type must be ACTIVE_SPEC_SOURCE_PACKAGE_RENDER")
    if receipt.get("coverage_scope") != "ACTIVE_SPEC":
        errors.append("receipt coverage_scope must be ACTIVE_SPEC")

    receipt_spec_id = receipt.get("active_spec_id")
    if not isinstance(receipt_spec_id, str) or not receipt_spec_id:
        errors.append("receipt active_spec_id must be non-empty string")

    receipt_anchors = receipt.get("selected_hld_anchor_ids")
    if not isinstance(receipt_anchors, list) or not all(isinstance(s, str) for s in receipt_anchors):
        errors.append("receipt selected_hld_anchor_ids must be list of strings")

    if receipt.get("target_materialization") != "NOT_MATERIALIZED":
        errors.append("receipt target_materialization must be NOT_MATERIALIZED")

    rendered = receipt.get("rendered_file")
    expected_rendered = AUTHORITATIVE_FILES["single_spec_input"]
    if rendered != expected_rendered:
        errors.append(f"receipt rendered_file must be {expected_rendered}")

    scope_file = receipt.get("coverage_scope_file")
    expected_scope_file = AUTHORITATIVE_FILES["hld_coverage_scope"]
    if scope_file != expected_scope_file:
        errors.append(f"receipt coverage_scope_file must be {expected_scope_file}")

    ledger_file = receipt.get("coverage_ledger_file")
    expected_ledger_file = AUTHORITATIVE_FILES["hld_coverage_ledger"]
    if ledger_file != expected_ledger_file:
        errors.append(f"receipt coverage_ledger_file must be {expected_ledger_file}")

    created_at = receipt.get("created_at")
    if not isinstance(created_at, str) or not created_at:
        errors.append("receipt created_at must be non-empty string")

    notes = receipt.get("notes")
    if not isinstance(notes, list):
        errors.append("receipt notes must be a list")

    # --- Consistency: receipt vs scope ---
    scope_spec_id = scope.get("active_spec_id")
    if isinstance(receipt_spec_id, str) and receipt_spec_id and scope_spec_id != receipt_spec_id:
        errors.append(
            f"receipt active_spec_id ({receipt_spec_id}) differs from scope ({scope_spec_id})"
        )

    scope_anchors = scope.get("selected_hld_anchor_ids")
    if (
        isinstance(receipt_anchors, list)
        and isinstance(scope_anchors, list)
        and receipt_anchors != scope_anchors
    ):
        errors.append("receipt selected_hld_anchor_ids differs from scope")

    if isinstance(receipt.get("coverage_scope"), str) and scope_mode != receipt.get("coverage_scope"):
        errors.append("receipt coverage_scope differs from scope coverage_scope")

    # --- File-ref existence ---
    if isinstance(rendered, str) and rendered and not (source_dir / rendered).is_file():
        errors.append(f"receipt rendered_file references missing file: {rendered}")

    if isinstance(scope_file, str) and scope_file and not (source_dir / scope_file).is_file():
        errors.append(f"receipt coverage_scope_file references missing file: {scope_file}")

    if isinstance(ledger_file, str) and ledger_file and not (source_dir / ledger_file).is_file():
        errors.append(f"receipt coverage_ledger_file references missing file: {ledger_file}")

    # --- Manifest entry ---
    manifest = load_json_dict(source_dir / SOURCE_MANIFEST_FILE).get("files", {})
    manifest_entry = manifest.get("active_spec_materialization_receipt")
    if not manifest_entry or not manifest_entry.get("present"):
        errors.append("manifest must include present entry for active_spec_materialization_receipt")
    elif not manifest_entry.get("sha256"):
        errors.append("manifest entry for receipt must include sha256")

    # --- Selected-anchor coverage via interpreter ---
    ledger_path = source_dir / AUTHORITATIVE_FILES["hld_coverage_ledger"]
    if ledger_path.is_file():
        try:
            ledger = json.loads(ledger_path.read_text(encoding="utf-8"))
        except (json.JSONDecodeError, OSError):
            errors.append("hld_coverage_ledger.json is malformed JSON")
            ledger = None

        if ledger is not None:
            from .hld_coverage_scope_interpretation import interpret_coverage_ledger_for_scope

            interp = interpret_coverage_ledger_for_scope(
                coverage_ledger=ledger, coverage_scope=scope,
            )
            if not interp.ok:
                errors.append(
                    f"coverage ledger interpretation failed: {'; '.join(interp.errors)}"
                )
            elif interp.blocking_items:
                blocked_ids = [item.get("hld_item_id", "?") for item in interp.blocking_items]
                errors.append(
                    f"selected anchors not covered: {', '.join(blocked_ids)}"
                )

    return errors


def validate_source_package(source_dir: Path) -> SourcePackageValidation:
    """Check required files are present and that any recorded manifest hashes
    still match the files on disk."""
    result = SourcePackageValidation()
    for filename in REQUIRED_FILES:
        if not (source_dir / filename).is_file():
            result.missing.append(filename)

    recorded = load_json_dict(source_dir / SOURCE_MANIFEST_FILE).get("files", {})
    for name, entry in recorded.items():
        filename = entry.get("filename")
        recorded_hash = entry.get("sha256")
        if not filename or recorded_hash is None:
            continue
        path = source_dir / filename
        if not path.is_file():
            result.missing.append(filename)
        elif _sha256(path) != recorded_hash:
            result.hash_mismatches.append(filename)

    result.semantic_errors = _validate_receipt_semantics(source_dir)
    return result


def materialize_specify_mirror(
    source_dir: Path,
    mirror_dir: Path,
) -> list[str]:
    """Regenerate the derived read-only mirror under .specify/source/.

    Idempotent and orphan-free: every HLDspec-managed name is removed first, then
    the current mirror set is rewritten, so a file dropped from the source package
    (or the constitution proposal) cannot persist in the mirror. Non-HLDspec
    content in the mirror directory is never touched. Markdown files get a
    GENERATED banner. Returns the list of mirrored filenames.
    """
    mirror_dir.mkdir(parents=True, exist_ok=True)
    # Wipe only HLDspec-managed names so orphans cannot survive a regeneration.
    for name in HLDSPEC_MANAGED_MIRROR_NAMES:
        stale = mirror_dir / name
        if stale.exists():
            stale.unlink()
    written: list[str] = []
    for filename in MIRROR_FILES:
        src = source_dir / filename
        if not src.is_file():
            continue
        dest = mirror_dir / filename
        dest.write_text(
            _expected_mirror_text(filename, src.read_text(encoding="utf-8")),
            encoding="utf-8",
        )
        written.append(filename)
    return written


def _expected_mirror_text(filename: str, source_text: str) -> str:
    """The exact text materialize_specify_mirror writes for a mirror file."""
    if filename.endswith(".md"):
        return f"{GENERATED_BANNER}\n\n{source_text}"
    return source_text


def mirror_freshness_blockers(source_dir: Path, mirror_dir: Path) -> list[str]:
    """Drift check for the derived .specify/source/ mirror.

    A mirror file is judged only against what materialize_specify_mirror would
    write today: every MIRROR_FILES entry present in the source package must
    exist in the mirror with exactly that content, and no other HLDspec-managed
    name may linger there (an orphan proves a stale materialisation). Files
    HLDspec does not manage are never judged.
    """
    blockers: list[str] = []
    expected: set[str] = set()
    for filename in MIRROR_FILES:
        src = source_dir / filename
        if not src.is_file():
            continue
        expected.add(filename)
        dest = mirror_dir / filename
        if not dest.is_file():
            blockers.append(
                f"Specify mirror is missing {filename} ({dest}) — "
                "re-materialise the mirror from the source package."
            )
        elif dest.read_text(encoding="utf-8") != _expected_mirror_text(
            filename, src.read_text(encoding="utf-8")
        ):
            blockers.append(
                f"Specify mirror is stale: {filename} ({dest}) does not match the "
                "source package — re-materialise the mirror; do not edit it."
            )
    for name in sorted(HLDSPEC_MANAGED_MIRROR_NAMES - expected):
        if (mirror_dir / name).exists():
            blockers.append(
                f"Specify mirror has orphan {name} ({mirror_dir / name}) from an "
                "older materialisation — re-materialise the mirror."
            )
    return blockers


def source_package_paths(target_root: Path, layout: str = "new") -> tuple[Path, Path]:
    """The single pointer-aware resolver for (authoritative source dir, mirror dir).

    Pointer-aware (Option C): when a valid ``.hldspec-run.json`` pointer names an
    external controller root, the *authoritative* source package resolves under the
    controller (the externalized HLDspec control plane). The mirror always stays at
    the target's ``.specify/source/`` because it is the delivered, in-target SpecKit
    runtime. Without a pointer this is byte-identical to legacy in-target resolution.

    Both the Journey 3 driver (read) and ``build_source_package_content`` (write)
    resolve through here, so read and write can never disagree on the location.
    """
    adapter = TargetWorkspaceAdapter(
        target_root=target_root,
        layout=layout,
        controller_root=control_paths.resolve_controller_root(target_root),
    )
    return adapter.source_package_dir, adapter.specify_source_mirror_dir


def source_package_split_brain(target_root: Path) -> Path | None:
    """Detect a source-package split-brain; return the conflicting in-target dir, else None.

    A completed externalization deletes the in-target ``.hldspec/`` entirely
    (``run_state.externalize_target_control_artifacts``), so a healthy external
    target never holds an in-target ``source_package.json``. When external mode is
    active AND an authoritative package exists in BOTH the controller and the
    target, ownership is ambiguous (interrupted externalization, or a manual
    in-target build). The caller must fail closed rather than silently pick one or
    repair — this only reports the conflict; it never deletes or reconciles.
    """
    controller = control_paths.resolve_controller_root(target_root)
    if controller is None:
        return None  # normal mode: in-target IS the single authoritative location.
    controller_pkg = controller / ".hldspec" / "source_package" / SOURCE_PACKAGE_FILE
    in_target = Path(target_root) / ".hldspec" / "source_package"
    if controller_pkg.is_file() and (in_target / SOURCE_PACKAGE_FILE).is_file():
        return in_target
    return None


