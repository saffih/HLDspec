def build_helper_recommendations(registry: dict | None = None) -> dict:
    """Advisory Journey 2 output: which helper is recommended for THIS package, and why.

    Helper capability facts (helper_id, status, authority levels) are **derived**
    from `hldspec/helper_registry.py` — the canonical source of truth for helper
    capabilities. This file is not a copy of the registry; it adds only advisory
    package-fit information. The not-implemented helper IDs come from the registry's
    `PLANNED_HELPER_IDS`, not an independent list here.

    It does NOT record which helper the targeteer selected
    (`.hldspec/helper_selection.json`, a future Journey 3 slice), an execution
    prompt (NextActionPacket), or unresolved questions (inquiry/gap ledger).
    """
    reg = helper_registry.build_registry() if registry is None else registry

    recommended = []
    for helper in helper_registry.implemented_helpers(reg):
        helper_id = helper["helper_id"]
        fit = _HELPER_PACKAGE_FIT.get(helper_id, {})
        recommended.append({
            "helper_id": helper_id,
            "status": helper["status"],                            # derived from registry
            "authority_levels": list(helper["authority_levels"]),  # derived from registry
            "package_fit": fit.get("package_fit", "unknown"),      # advisory (Journey 2)
            "target_fit": fit.get("target_fit", "unknown"),        # advisory (Journey 2)
            "rationale": fit.get("rationale", ""),                 # advisory (Journey 2)
        })

    unsupported = [
        {
            "helper_id": helper_id,
            "status": "not_implemented",
            "reason": (
                "Contracted in docs/JOURNEY3_HELPER_CONTRACT.md / "
                "docs/HELPER_BOOTSTRAP_CONTRACT.md but not yet an operational helper "
                "in the helper registry."
            ),
        }
        for helper_id in helper_registry.PLANNED_HELPER_IDS
    ]

    return {
        "schema_version": 1,
        "default_helper": helper_registry.default_helper_id(reg),  # derived from registry
        "registry_provenance": {
            "schema_version": reg.get("schema_version"),
            "registry_sha256": helper_registry.registry_sha256(reg),
        },
        "recommended_helpers": recommended,
        "unsupported_helpers": unsupported,
        "required_capabilities": [
            "spec-driven implementation guidance over an anchored source package",
        ],
        "human_override_allowed": True,
        "notes": [
            "Advisory Journey 2 output. Helper capability facts are derived from "
            "hldspec/helper_registry.py; this file is not a copy of the registry.",
            "Records which helper is recommended for this package, not which helper "
            "was selected. Selection is .hldspec/helper_selection.json (future).",
            "Regenerate after structural HLD changes or after the helper registry changes.",
        ],
    }


def build_initial_hld_coverage_ledger(
    ref_map: dict,
    spec_input_text: str,
) -> list[dict]:
    """Build the first live Journey 2 coverage ledger from package artifacts.

    This is a producer only. It records source-package coverage evidence for a
    future gate, but it does not approve or block anything itself.
    """
    anchors = ref_map.get("anchors", {}) if isinstance(ref_map, dict) else {}
    ledger: list[dict] = []
    for anchor_id in sorted(anchors):
        meta = anchors.get(anchor_id) or {}
        cited = f"({anchor_id})" in spec_input_text
        risk = str(meta.get("risk") or "").upper() or None
        if risk not in journey2_hld_coverage_contracts.VALID_RISK_LEVELS:
            risk = None
        item = {
            "hld_item_id": anchor_id,
            "source_section": anchor_id,
            "source_text": str(meta.get("title") or meta.get("heading") or anchor_id),
            "item_type": journey2_hld_coverage_contracts.ITEM_REQUIREMENT,
            "status": (
                journey2_hld_coverage_contracts.STATUS_COVERED_IN_SDD
                if cited
                else journey2_hld_coverage_contracts.STATUS_NOT_COVERED
            ),
            "sdd_section": "speckit_single_spec_input.md" if cited else None,
            "design_decision": None,
            "research_required": False,
            "research_evidence": None,
            "clarification_required": False,
            "assumption": None,
            "acceptance_criteria": None,
            "test_mapping": None,
            "risk": risk,
        }
        journey2_hld_coverage_contracts.validate_coverage_item(item)
        ledger.append(item)
    return ledger


def _resolve_active_spec(backlog: object) -> tuple[str, dict]:
    """Validate an explicit active-spec backlog and return (spec_id, spec_dict).

    Fails fast with ValueError on any invalid input. Never falls back to FULL_HLD.
    """
    validation = spec_backlog.validate_spec_backlog(backlog)
    if not validation.ok:
        raise ValueError(
            "active_spec_backlog is invalid: " + "; ".join(validation.errors)
        )

    active_spec_id = backlog["active_spec_id"]  # type: ignore[index]
    if not isinstance(active_spec_id, str) or not active_spec_id:
        raise ValueError("active_spec_id is required for active-spec source-package mode")

    active_spec = None
    for s in backlog["specs"]:  # type: ignore[index]
        if s["spec_id"] == active_spec_id:
            active_spec = s
            break

    if active_spec is None:
        raise ValueError(f"active spec not found: {active_spec_id}")

    if active_spec["status"] != "SELECTED":
        raise ValueError(f"active spec must have status SELECTED: {active_spec_id}")

    if active_spec["target_materialization"] != "NOT_MATERIALIZED":
        raise ValueError(f"active spec must not be materialized: {active_spec_id}")

    return active_spec_id, active_spec


def _build_active_spec_source_package_receipt(
    *,
    active_spec_id: str,
    active_spec: dict,
    source_refs: list[str],
    created_at: str,
) -> dict:
    return {
        "schema_version": 1,
        "receipt_type": "ACTIVE_SPEC_SOURCE_PACKAGE_RENDER",
        "active_spec_id": active_spec_id,
        "active_spec_title": active_spec.get("title", ""),
        "coverage_scope": "ACTIVE_SPEC",
        "selected_hld_anchor_ids": list(active_spec.get("hld_anchor_ids", [])),
        "target_materialization": "NOT_MATERIALIZED",
        "rendered_file": AUTHORITATIVE_FILES["single_spec_input"],
        "coverage_scope_file": AUTHORITATIVE_FILES["hld_coverage_scope"],
        "coverage_ledger_file": AUTHORITATIVE_FILES["hld_coverage_ledger"],
        "source_refs": list(source_refs),
        "created_at": created_at,
        "notes": [
            "Records active-spec rendering into the source package only.",
            "Does not mark target materialization or mutate the spec backlog.",
        ],
    }


def build_source_package_content(
    target_root: Path,
    hld_text: str,
    *,
    hld_source_ref: str,
    state: str = "SOURCE_PACKAGE_IMPORTED",
    project_name: str = "",
    layout: str = "new",
    materialize_mirror: bool = True,
    source_sha256: str | None = None,
    active_spec_backlog: object | None = None,
) -> SourcePackageBuild:
    """Real content flow: turn an HLD into the full source-package content.

    Writes HLD.md, HLD.marked.md, hld_reference_map.json,
    speckit_single_spec_input.md, then the manifest + metadata, then optionally
    regenerates the .specify/source/ mirror. Returns a structured build result
    (does not raise on content findings — unsupported claims surface for the
    gate, matching the SourcePackageValidation pattern).

    When *active_spec_backlog* is provided, the single_spec_input and coverage
    scope are rendered for the selected active spec instead of the full HLD.
    The coverage ledger is built from the actual written spec input (full-HLD
    in FULL_HLD mode, active-spec rendered content in ACTIVE_SPEC mode) so
    ledger evidence always matches the written file.
    """
    # Imported here to avoid an import cycle (those modules import this one).
    from . import hld_marking, single_spec_input, implementation_slicing, engineering_selection

    active_spec_id: str | None = None
    active_spec: dict | None = None
    if active_spec_backlog is not None:
        active_spec_id, active_spec = _resolve_active_spec(active_spec_backlog)

    # Resolve through the single pointer-aware resolver so write lands where the
    # driver reads (controller root in external mode, in-target otherwise).
    source_dir, mirror_dir = source_package_paths(target_root, layout=layout)
    source_dir.mkdir(parents=True, exist_ok=True)

    (source_dir / AUTHORITATIVE_FILES["hld"]).write_text(hld_text, encoding="utf-8")
    (source_dir / AUTHORITATIVE_FILES["marked_hld"]).write_text(
        hld_marking.build_marked_hld(hld_text), encoding="utf-8"
    )
    ref_map = hld_marking.build_reference_map(hld_text)
    write_json_dict(source_dir / AUTHORITATIVE_FILES["reference_map"], ref_map)

    full_hld_spec_input = single_spec_input.build_single_spec_input(hld_text, project_name=project_name)

    if active_spec_backlog is not None:
        written_spec_input = spec_backlog.render_active_spec_to_single_spec_input(active_spec_backlog)
    else:
        written_spec_input = full_hld_spec_input

    (source_dir / AUTHORITATIVE_FILES["single_spec_input"]).write_text(written_spec_input, encoding="utf-8")

    write_json_dict(
        source_dir / AUTHORITATIVE_FILES["hld_coverage_ledger"],
        build_initial_hld_coverage_ledger(ref_map, written_spec_input),
    )

    now = utc_now()
    write_json_dict(
        source_dir / AUTHORITATIVE_FILES["spec_backlog"],
        spec_backlog.build_advisory_spec_backlog(
            ref_map,
            created_at=now,
            updated_at=now,
            source_refs=[hld_source_ref],
        ),
    )

    if active_spec_id is not None and active_spec is not None:
        scope = hld_coverage_scope.build_active_spec_coverage_scope_from_selected_spec(
            active_spec_id=active_spec_id,
            selected_spec=active_spec,
            source_refs=[hld_source_ref],
            notes=["Explicit active-spec source-package mode."],
        )
    else:
        scope = hld_coverage_scope.build_full_hld_coverage_scope(
            source_refs=[hld_source_ref],
            notes=["Full-HLD source-package coverage scope. Existing behavior preserved."],
        )

    write_json_dict(
        source_dir / AUTHORITATIVE_FILES["hld_coverage_scope"],
        scope,
    )

    if active_spec_id is not None and active_spec is not None:
        receipt = _build_active_spec_source_package_receipt(
            active_spec_id=active_spec_id,
            active_spec=active_spec,
            source_refs=[hld_source_ref],
            created_at=now,
        )
        write_json_dict(
            source_dir / AUTHORITATIVE_FILES["active_spec_materialization_receipt"],
            receipt,
        )

    for filename, content in implementation_slicing.build_implementation_slicing_artifacts().items():
        (source_dir / filename).write_text(content, encoding="utf-8")

    (source_dir / AUTHORITATIVE_FILES["engineering_guidelines"]).write_text(
        engineering_selection.render_engineering_guidelines_md(hld_text, project_name=project_name),
        encoding="utf-8",
    )

    helper_recommendations = build_helper_recommendations()
    write_json_dict(
        source_dir / AUTHORITATIVE_FILES["helper_recommendations"],
        helper_recommendations,
    )
    # Advisory Journey 2 architecture slot. Grounds only helper_recommendation
    # (registry-derived); the human-owned fields are emitted empty so the artifact
    # honestly validates ACTION until authored. Never required, never mirrored.
    write_json_dict(
        source_dir / AUTHORITATIVE_FILES["architecture_package"],
        journey2_architecture_package.build_architecture_package(
            helper_recommendation=helper_recommendations,
        ),
    )

    valid_anchors = set(ref_map["anchors"].keys())
    unsupported = single_spec_input.find_unsupported_claims(full_hld_spec_input, valid_anchors)
    marking_errors = hld_marking.anchor_integrity_errors(hld_text)

    write_source_package(
        source_dir,
        hld_source_ref=hld_source_ref,
        state=state,
        target_root=target_root,
        source_sha256=source_sha256 or _source_file_sha256(hld_source_ref),
    )
    validation = validate_source_package(source_dir)
    mirrored: list[str] = []
    if materialize_mirror:
        mirrored = materialize_specify_mirror(source_dir, mirror_dir)

    try:
        agent_handoff_pack.write_agent_handoff_pack_artifacts(target_root)
    except Exception as exc:
        raise RuntimeError(
            f"failed to write Journey 3 Agent Handoff Pack artifacts under {target_root}: {exc}"
        ) from exc

    return SourcePackageBuild(
        source_dir=source_dir,
        anchor_count=len(valid_anchors),
        unsupported_claims=unsupported,
        validation=validation,
        mirrored=mirrored,
        marking_errors=marking_errors,
    )
