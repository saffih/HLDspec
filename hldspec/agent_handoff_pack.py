"""Agent Handoff Pack generator (Journey 3).

The Agent Handoff Pack is the current per-run/per-slice instruction, check, and
evidence package handed to a target-side agent. In the controller/target/agent
model:

- the (future) **agent bridge** discovers the controller<->target binding;
- the **Agent Handoff Pack** instructs (this module);
- the (future) **command envelope** bounds one proposed action;
- the **driver** validates/gates/recommends the next safe action; the **human**
  approves protected transitions; the agent executes only inside approved scope.

The handoff pack is **not** the bridge, is **not** approval state, and executes
nothing. It only renders guidance and resolved evidence paths and stops on
blockers.

Pointer-aware (Option C): the source package and packet live in the resolved
control plane — ``target/.hldspec/...`` in default/no-pointer mode and
``controller_root/.hldspec/...`` when a ``.hldspec-run.json`` pointer names an
external controller. The same resolution is used by Journey 3
(``hld_source_package.source_package_paths``); we resolve through the adapter
here to avoid a circular import. Helper runtime (``.specify/source/``,
``specs/``) always stays target-local.

History: this module was named ``mediator_guidance`` ("Journey 3 mediator
guidance"). ``hldspec/mediator_guidance.py`` remains as a deprecated
compatibility shim. On-disk artifact names (``mediator/``, ``mediator_packet.json``,
``*_MEDIATOR*.md``) and the packet JSON keys are kept for downstream consumers;
their rename is tracked in backlog P1-018.
"""
